void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim)
{
  static int cnt = 0;
  if(htim->Instance == TIM4)
  {
    cnt++;
    if(cnt %10 == 0)
    {
        mpu_dmp_get_data(&pitch, &roll, &yaw);
        Motor_speed_R = PID_Proc(&pid_motor_R,yaw);
        Motor_speed_L = PID_Proc(&pid_motor_L,yaw);
        _10ms_flag = 1;
    }

    if(cnt %20 == 0)
    {
        if(zhixing == 1)
        {
            GET_ADC_Value();
            if(ADC_Value[0] == 1 || ADC_Value[1] == 1)
            {
                turn_cnt++;//ÍäµÀ¼ÆÊý++
                turn_cnt %= 18;
                xuanzhuan = 1;
                xuanzhuan_start = 1;
                zhixing   = 0;
            }
        }
    }

    if(cnt %50 == 0)
    {
        _50ms_flag = 1;
    }

    if(cnt %100 == 0)
    {
        HAL_GPIO_TogglePin(GPIOC,GPIO_PIN_13);
    }

    if(cnt %5000 == 0)
    {
        cnt = 0;
    }

  }
}


